respuesta = input("Ingrese un numero romano para ver su valor: ")

if (respuesta == "I"):
	numero = "1"
elif (respuesta == "V"):
	numero = "5"
elif (respuesta == "X"):
	numero = "10"
elif (respuesta == "L"):
	numero = "50"
elif (respuesta == "C"):
	numero = "100"
elif (respuesta == "M"):
	numero = "1000"

else:
	numero = "Error, usted no eligio un numero romano"

