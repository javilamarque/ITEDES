num = int(input("Ingrese un numero del 1 al 7 para descubrir el dia: "))

if (num == 1):
	print("Lunes")
elif (num == 2):
	print("Martes")
elif (num == 3):
	print("Miercoles")
elif (num == 4):
	print("Jueves")
elif (num == 5):
	print("Viernes")
elif (num == 6):
	print("Sabado")
elif (num == 7):
	print("Domingo")

else:
	print("Usted eligio un numero incorrecto fuera del 1 al 7 ")
